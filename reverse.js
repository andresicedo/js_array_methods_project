/*
REVERSE METHOD
The reverse() method reverses an array in place. The first array 
element becomes the last, and the last array element becomes the first.

Syntax : arr.reverse()
*/
let cities = ["Houston", "Dallas", "Austin", "El Paso", "San Antonio"];

let reversed = cities.reverse();
console.log(reversed);


let numbers = [1, 2, "3", 4, "five", true];
let revNumbers = numbers.reverse();
console.log(revNumbers);
